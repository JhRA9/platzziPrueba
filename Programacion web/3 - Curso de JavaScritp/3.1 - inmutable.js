// Tipo de dato primivito - inmutable
let numero = 10
numero = numero + 10
console.log(numero); // 11

let esVerdadero = true
esVerdadero = false
console.log(esVerdadero); // false

// Lo que sucede en la línea 3 es que se está reasignando el valor de la variable numero. Esto es posible porque los tipos de datos primitivos son inmutables, lo que significa que no pueden ser alterados. En este caso, el valor de la variable numero se incrementa en 1 y se guarda en la misma variable.